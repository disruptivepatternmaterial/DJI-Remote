void m5stack_basic_v27_display_set_brightness(uint8_t brightness) { (void)brightness; }
esp_lcd_panel_handle_t m5stack_basic_v27_get_display_handle(void) { return panel_handle; }

void m5stack_basic_v27_display_clear(uint16_t color) {
    if (!panel_handle) return;
    const int ch = 30;
    int total = PHYS_W * ch;
    uint16_t *buf = malloc(total * sizeof(uint16_t));
    if (!buf) return;
    for (int i = 0; i < total; i++) buf[i] = color;
    for (int y = 0; y < PHYS_H; y += ch) {
        int h = (y + ch > PHYS_H) ? (PHYS_H - y) : ch;
        esp_lcd_panel_draw_bitmap(panel_handle, 0, y, PHYS_W, y + h, buf);
    }
    free(buf);
}

void m5stack_basic_v27_display_show_boot_logo(void) {
    m5stack_basic_v27_display_clear(M5_COLOR_BLACK);
}

static void draw_char_scaled(int x, int y, char c, uint16_t color, int scale) {
    if (!panel_handle) return;
    int idx = (c >= ' ' && c <= 'z') ? (c - ' ') : 0;
    for (int row = 0; row < 8; row++) {
        uint8_t line = font8x8_plus11[idx][row];
        for (int col = 0; col < 8; col++) {
            if (line & (0x80 >> col)) {
                for (int sy = 0; sy < scale; sy++)
                    for (int sx = 0; sx < scale; sx++) {
                        int px = SCALE_X(x + col * scale + sx);
                        int py = y + row * scale + sy;
                        if (px < PHYS_W && py < PHYS_H)
                            esp_lcd_panel_draw_bitmap(panel_handle, px, py, px + 1, py + 1, &color);
                    }
            }
        }
    }
}

void m5stack_basic_v27_display_print(int x, int y, const char *text, uint16_t color) {
    m5stack_basic_v27_display_print_scaled(x, y, text, color, 1);
}

void m5stack_basic_v27_display_print_scaled(int x, int y, const char *text, uint16_t color, int scale) {
    if (!panel_handle || !text) return;
    int cx = x, cy = y, cw = 8 * scale, ch = 8 * scale, sp = ch + 2;
    for (const char *p = text; *p; p++) {
        if (*p == '\n') { cx = x; cy += sp; continue; }
        draw_char_scaled(cx, cy, *p, color, scale);
        cx += cw;
        if (SCALE_X(cx) > PHYS_W - SCALE_W(cw)) { cx = x; cy += sp; }
    }
}

void m5stack_basic_v27_display_draw_bitmap(int x, int y, int width, int height, const uint8_t *bitmap, uint16_t color, uint16_t bg_color) {
    if (!panel_handle || !bitmap) return;
    int px = SCALE_X(x), py = y, pw = SCALE_W(width), ph = height;
    if (px >= PHYS_W || py >= PHYS_H || pw < 1 || ph < 1) return;
    int byte_width = (width + 7) / 8;
    for (int row = 0; row < height && (py + row) < PHYS_H; row++) {
        for (int col = 0; col < width; col++) {
            int pcol = SCALE_X(x + col);
            if (pcol >= PHYS_W) continue;
            int by = row * byte_width + col / 8, bi = 7 - (col % 8);
            uint16_t pixel_color = ((bitmap[by] >> bi) & 1) ? color : bg_color;
            esp_lcd_panel_draw_bitmap(panel_handle, pcol, py + row, pcol + 1, py + row + 1, &pixel_color);
        }
    }
}

void m5stack_basic_v27_display_fill_circle(int x, int y, int radius, uint16_t color) {
    int size = radius * 2;
    m5stack_basic_v27_display_fill_rect(x - radius, y - radius, size, size, color);
}

void m5stack_basic_v27_display_fill_rect(int x, int y, int width, int height, uint16_t color) {
    if (!panel_handle) return;
    int px = SCALE_X(x), py = y, pw = SCALE_W(width), ph = height;
    if (px >= PHYS_W || py >= PHYS_H) return;
    if (px + pw > PHYS_W) pw = PHYS_W - px;
    if (py + ph > PHYS_H) ph = PHYS_H - py;
    if (pw <= 0 || ph <= 0) return;
    size_t sz = (size_t)pw * ph * sizeof(uint16_t);
    uint16_t *buf = heap_caps_malloc(sz, MALLOC_CAP_DMA);
    if (!buf) return;
    for (int i = 0; i < pw * ph; i++) buf[i] = color;
    esp_lcd_panel_draw_bitmap(panel_handle, px, py, px + pw, py + ph, buf);
    heap_caps_free(buf);
}

