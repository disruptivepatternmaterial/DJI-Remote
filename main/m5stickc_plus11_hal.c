int m5stack_basic_v27_power_init(void) {
    gpio_config_t pwr = {
        .intr_type = GPIO_INTR_DISABLE,
        .mode = GPIO_MODE_OUTPUT,
        .pin_bit_mask = (1ULL << PWR_EN_PIN),
        .pull_down_en = 0, .pull_up_en = 0,
    };
    gpio_config(&pwr);
    gpio_set_level(PWR_EN_PIN, 1);
    return 0;
}

int m5stack_basic_v27_display_init(void) {
    gpio_config_t rst = {
        .intr_type = GPIO_INTR_DISABLE, .mode = GPIO_MODE_OUTPUT,
        .pin_bit_mask = (1ULL << LCD_RST_PIN), .pull_down_en = 0, .pull_up_en = 0,
    };
    gpio_config(&rst);
    gpio_config_t dc = {
        .intr_type = GPIO_INTR_DISABLE, .mode = GPIO_MODE_OUTPUT,
        .pin_bit_mask = (1ULL << LCD_DC_PIN), .pull_down_en = 0, .pull_up_en = 0,
    };
    gpio_config(&dc);
    gpio_config_t cs = {
        .intr_type = GPIO_INTR_DISABLE, .mode = GPIO_MODE_OUTPUT,
        .pin_bit_mask = (1ULL << LCD_CS_PIN), .pull_down_en = 0, .pull_up_en = 0,
    };
    gpio_config(&cs);
    gpio_set_level(LCD_CS_PIN, 1);
    gpio_set_level(LCD_RST_PIN, 0);
    vTaskDelay(pdMS_TO_TICKS(10));
    gpio_set_level(LCD_RST_PIN, 1);
    vTaskDelay(pdMS_TO_TICKS(10));

    spi_bus_config_t buscfg = {
        .mosi_io_num = LCD_MOSI_PIN, .miso_io_num = -1, .sclk_io_num = LCD_SCLK_PIN,
        .quadwp_io_num = -1, .quadhd_io_num = -1, .max_transfer_sz = 4096,
    };
    esp_err_t ret = spi_bus_initialize(VSPI_HOST, &buscfg, 1);
    if (ret != ESP_OK) return ret;

    esp_lcd_panel_io_spi_config_t io_config = {
        .dc_gpio_num = LCD_DC_PIN, .cs_gpio_num = LCD_CS_PIN,
        .pclk_hz = 26 * 1000 * 1000, .lcd_cmd_bits = 8, .lcd_param_bits = 8,
        .spi_mode = 0, .trans_queue_depth = 10,
    };
    ret = esp_lcd_new_panel_io_spi((esp_lcd_spi_bus_handle_t)VSPI_HOST, &io_config, &io_handle);
    if (ret != ESP_OK) return ret;

    esp_lcd_panel_dev_config_t panel_config = {
        .reset_gpio_num = LCD_RST_PIN, .color_space = ESP_LCD_COLOR_SPACE_BGR,
        .bits_per_pixel = 16, .flags = { .reset_active_high = 0 }, .vendor_config = NULL,
    };
    ret = esp_lcd_new_panel_st7789(io_handle, &panel_config, &panel_handle);
    if (ret != ESP_OK) return ret;
    esp_lcd_panel_reset(panel_handle);
    esp_lcd_panel_init(panel_handle);
    esp_lcd_panel_invert_color(panel_handle, true);
    esp_lcd_panel_swap_xy(panel_handle, true);
    esp_lcd_panel_mirror(panel_handle, false, true);
    esp_lcd_panel_set_gap(panel_handle, 40, 52);
    esp_lcd_panel_disp_on_off(panel_handle, true);
    m5stack_basic_v27_display_clear(M5_COLOR_BLACK);
    return 0;
}

int m5stack_basic_v27_buttons_init(void) {
    gpio_config_t io = {
        .intr_type = GPIO_INTR_DISABLE, .mode = GPIO_MODE_INPUT,
        .pin_bit_mask = (1ULL << BTN_A_PIN) | (1ULL << BTN_B_PIN) | (1ULL << BTN_C_PIN),
        .pull_down_en = 0, .pull_up_en = 0,
    };
    gpio_config(&io);
    return 0;
}

int m5stack_basic_v27_init(void) {
    m5stack_basic_v27_power_init();
    m5stack_basic_v27_display_init();
    m5stack_basic_v27_buttons_init();
    return 0;
}

